package model;

/**
 *
 * @author emsin
 */
public class Customer {
    private int id;
    private String name, phone;
    private int room_id;

    public Customer(int id, String name, String phone, int room_id) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.room_id = room_id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getRoom_id() {
        return room_id;
    }

    public void setRoom_id(int room_id) {
        this.room_id = room_id;
    }
    
}
